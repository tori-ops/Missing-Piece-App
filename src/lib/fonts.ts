export function getFontFamily(fontName?: string | null): string {
  if (!fontName) return "'Poppins', sans-serif";
  const font = AVAILABLE_FONTS.find(f => f.name === fontName);
  return font ? font.family : "'Poppins', sans-serif";
}

export function getHeaderFontFamily(fontName?: string | null): string {
  if (!fontName) return "'Playfair Display', serif";
  const font = HEADER_FONTS.find(f => f.name === fontName);
  return font ? font.family : "'Playfair Display', serif";
}

export function getBodyFontFamily(fontName?: string | null): string {
  if (!fontName) return "'Poppins', sans-serif";
  const font = BODY_FONTS.find(f => f.name === fontName);
  return font ? font.family : "'Poppins', sans-serif";
}

export function getFontImportLinks(): string {
  // Google Fonts import for all available fonts
  const allFonts = [...AVAILABLE_FONTS, ...HEADER_FONTS, ...BODY_FONTS];
  const uniqueFontNames = Array.from(new Set(allFonts.map(f => f.name.replace(/ /g, '+')))).join('|');
  return `https://fonts.googleapis.com/css2?family=${uniqueFontNames}&display=swap`;
}
